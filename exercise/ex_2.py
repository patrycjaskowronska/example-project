# kwadrat 

a = 10

obwod = a * 4
pole = a * a

print("Obwód kwadratu wynosi " + str(obwod) + ", a pole " + str(pole) + ".")

# prostokat

a = 5
b = 10

obwod = 2 * a + 2 * b
pole = a * b

print("Obwód prostokąta wynosi " + str(obwod) + ", a pole " + str(pole) + ".")

# kolo

r = 7
pi = 3.14

obwod = 2 * pi * r
pole = pi * r * r

print("Obwód koła wynosi " + str(obwod) + ", a pole " + str(pole) + ".")

# trapez

a = 3
b = 5
c = 7
h = 6

obwod = a + 2 * b + c
pole = 1/2 * h * (a + c)

print("Obwód trapezu wynosi " + str(obwod) + ", a pole " + str(pole) + ".")

